public class StructIdBroaderTerm {
    private String idBroaderTerm;
    private String idGroupBroaderTerm;
    private String lexicalValueTerm;
    private String lexicalValueGroup;

    public String getIdBroaderTerm() {
        return idBroaderTerm;
    }

    public void setIdBroaderTerm(String idBroaderTerm) {
        this.idBroaderTerm = idBroaderTerm;
    }

    public String getIdGroupBroaderTerm() {
        return idGroupBroaderTerm;
    }

    public void setIdGroupBroaderTerm(String idGroupBroaderTerm) {
        this.idGroupBroaderTerm = idGroupBroaderTerm;
    }

    public String getLexicalValueTerm() {
        return lexicalValueTerm;
    }

    public void setLexicalValueTerm(String lexicalValueTerm) {
        this.lexicalValueTerm = lexicalValueTerm;
    }

    public String getLexicalValueGroup() {
        return lexicalValueGroup;
    }

    public void setLexicalValueGroup(String lexicalValueGroup) {
        this.lexicalValueGroup = lexicalValueGroup;
    }
    
    
    
}
