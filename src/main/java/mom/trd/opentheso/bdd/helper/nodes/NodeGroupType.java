package mom.trd.opentheso.bdd.helper.nodes;

/**
 *
 * @author miled.rousset
 */
public class NodeGroupType {

    private String code;
    private String label;
    private String skosLabel;

    public NodeGroupType() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getSkosLabel() {
        return skosLabel;
    }

    public void setSkosLabel(String skosLabel) {
        this.skosLabel = skosLabel;
    }


}
